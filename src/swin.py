def get_window_size(x_size, window_size, shift_size=None):
	"""Computing window size based on: "Liu et al.,
	Swin Transformer: Hierarchical Vision Transformer using Shifted Windows
	<https://arxiv.org/abs/2103.14030>"
	https://github.com/microsoft/Swin-Transformer

	 Args:
		x_size: input size.
		window_size: local window size.
		shift_size: window shifting size.
	"""
	
	use_window_size = list(window_size)
	
	if shift_size is not None:
		use_shift_size = list(shift_size)
	
	for i in range(len(x_size)):
		if x_size[i] <= window_size[i]:
			use_window_size[i] = x_size[i]
			if shift_size is not None:
				use_shift_size[i] = 0
	
	if shift_size is None:
		return tuple(use_window_size)
	else:
		return tuple(use_window_size), tuple(use_shift_size)


def window_partition(x, window_size):
	"""
	Args:
		x: (B, D, H, W, C)
		window_size (tuple[int]): window size

	Returns:
		windows: (B*num_windows, window_size*window_size, C)
	"""
	
	input_shape = tf.shape(x)
	B, D, H, W, C = (
		input_shape[0],
		input_shape[1],
		input_shape[2],
		input_shape[3],
		input_shape[4],
	)
	
	x = tf.reshape(
		x,
		[
			B,
			D // window_size[0], window_size[0],
			H // window_size[1], window_size[1],
			W // window_size[2], window_size[2],
			C
		]
	)
	
	x = tf.transpose(x, perm=[0, 1, 3, 5, 2, 4, 6, 7])
	windows = tf.reshape(x, [-1, window_size[0] * window_size[1] * window_size[2], C])
	
	return windows


def window_reverse(windows, window_size, B, D, H, W):
	"""
	Args:
		windows: (B*num_windows, window_size, window_size, C)
		window_size (tuple[int]): Window size
		H (int): Height of image
		W (int): Width of image

	Returns:
		x: (B, D, H, W, C)
	"""
	x = tf.reshape(
		windows,
		[
			B,
			D // window_size[0],
			H // window_size[1],
			W // window_size[2],
			window_size[0],
			window_size[1],
			window_size[2],
			-1
		]
	)
	x = tf.transpose(x, perm=[0, 1, 4, 2, 5, 3, 6, 7])
	x = tf.reshape(x, [B, D, H, W, -1])
	return x


def compute_mask(D, H, W, window_size, shift_size):
	img_mask = np.zeros((1, D, H, W, 1))
	cnt = 0
	for d in slice(-window_size[0]), slice(-window_size[0], -shift_size[0]), slice(-shift_size[0], None):
		for h in slice(-window_size[1]), slice(-window_size[1], -shift_size[1]), slice(-shift_size[1], None):
			for w in slice(-window_size[2]), slice(-window_size[2], -shift_size[2]), slice(-shift_size[2], None):
				img_mask[:, d, h, w, :] = cnt
				cnt = cnt + 1
	mask_windows = window_partition(img_mask, window_size)
	mask_windows = tf.squeeze(mask_windows, axis=-1)
	attn_mask = tf.expand_dims(mask_windows, axis=1) - tf.expand_dims(mask_windows, axis=2)
	attn_mask = tf.where(attn_mask != 0, -100.0, attn_mask)
	attn_mask = tf.where(attn_mask == 0, 0.0, attn_mask)
	return attn_mask


class StochasticDepth(layers.Layer):
	"""Stochastic Depth module.
	It is also referred to as Drop Path in `timm`.
	References:
		(1) github.com:rwightman/pytorch-image-models
	"""
	
	def __init__(self, drop_path, **kwargs):
		super(StochasticDepth, self).__init__(**kwargs)
		self.drop_path = drop_path
	
	def get_config(self):
		config = super(StochasticDepth, self).get_config()
		config.update({
			"drop_path": self.drop_path,
		})
		return config
	
	def call(self, inputs, training=None):
		if training:
			keep_prob = tf.cast(1 - self.drop_path, inputs.dtype)
			shape = (tf.shape(inputs)[0],) + (1,) * (len(tf.shape(inputs)) - 1)
			random_tensor = keep_prob + tf.random.uniform(shape, 0, 1, dtype=inputs.dtype)
			random_tensor = tf.floor(random_tensor)
			return (inputs / keep_prob) * random_tensor
		return inputs


class Mlp(layers.Layer):
	""" Multilayer perceptron."""
	
	def __init__(
		self,
		hidden_features,
		out_features,
		drop=0.0,
		**kwargs
	):
		super().__init__(**kwargs)
		self.fc1 = layers.Dense(hidden_features)
		self.fc2 = layers.Dense(out_features)
		self.act = layers.Activation('gelu')
		self.drop = layers.Dropout(drop)
	
	def call(self, x):
		x = self.fc1(x)
		x = self.act(x)
		x = self.drop(x)
		x = self.fc2(x)
		x = self.drop(x)
		return x

class WindowAttention3D(layers.Layer):
	""" Window based multi-head self attention (W-MSA) module with relative position bias.
	It supports both of shifted and non-shifted window.
	Args:
		dim (int): Number of input channels.
		window_size (tuple[int]): The temporal length, height and width of the window.
		num_heads (int): Number of attention heads.
		qkv_bias (bool, optional):  If True, add a learnable bias to query, key, value. Default: True
		qk_scale (float | None, optional): Override default qk scale of head_dim ** -0.5 if set
		attn_drop (float, optional): Dropout ratio of attention weight. Default: 0.0
		proj_drop (float, optional): Dropout ratio of output. Default: 0.0
	"""
	
	def __init__(
		self,
		dim,
		window_size,
		num_heads,
		qkv_bias=True,
		qk_scale=None,
		attn_drop=0.,
		proj_drop=0.,
		**kwargs
	):
		super().__init__(**kwargs)
		# variables
		self.dim = dim
		self.window_size = window_size
		self.num_heads = num_heads
		head_dim = dim // num_heads
		self.scale = qk_scale or head_dim ** -0.5
		
		# layers
		self.qkv = layers.Dense(dim * 3, use_bias=qkv_bias)
		self.attn_drop = layers.Dropout(attn_drop)
		self.proj = layers.Dense(dim)
		self.proj_drop = layers.Dropout(proj_drop)
	
	def get_relative_position_index(self, window_depth, window_height, window_width):
		y_y, z_z, x_x = tf.meshgrid(
			range(window_width), range(window_depth), range(window_height)
		)
		coords = tf.stack([z_z, y_y, x_x], axis=0)
		coords_flatten = tf.reshape(coords, [3, -1])
		relative_coords = coords_flatten[:, :, None] - coords_flatten[:, None, :]
		relative_coords = tf.transpose(relative_coords, perm=[1, 2, 0])
		z_z = (relative_coords[:, :, 0] + window_depth - 1) * (2 * window_height - 1) * (2 * window_width - 1)
		x_x = (relative_coords[:, :, 1] + window_height - 1) * (2 * window_width - 1)
		y_y = (relative_coords[:, :, 2] + window_width - 1)
		relative_coords = tf.stack([z_z, x_x, y_y], axis=-1)
		return tf.reduce_sum(relative_coords, axis=-1)
	
	def build(self, input_shape):
		self.relative_position_bias_table = self.add_weight(
			shape=(
				(2 * self.window_size[0] - 1) *
				(2 * self.window_size[1] - 1) *
				(2 * self.window_size[2] - 1),
				self.num_heads,
			),
			initializer="zeros",
			trainable=True,
			name="relative_position_bias_table",
		)
		self.relative_position_index = self.get_relative_position_index(
			self.window_size[0], self.window_size[1], self.window_size[2]
		)
		super().build(input_shape)
	
	def call(self, x, mask=None):
		input_shape = tf.shape(x)
		B_, N, C = (
			input_shape[0],
			input_shape[1],
			input_shape[2],
		)
		
		qkv = self.qkv(x)
		qkv = tf.reshape(qkv, [B_, N, 3, self.num_heads, C // self.num_heads])
		qkv = tf.transpose(qkv, perm=[2, 0, 3, 1, 4])
		q, k, v = tf.split(qkv, 3, axis=0)
		
		q = tf.squeeze(q, axis=0) * self.scale
		k = tf.squeeze(k, axis=0)
		v = tf.squeeze(v, axis=0)
		attn = tf.linalg.matmul(q, k, transpose_b=True)
		
		relative_position_bias = tf.gather(
			self.relative_position_bias_table, self.relative_position_index[:N, :N]
		)
		relative_position_bias = tf.reshape(relative_position_bias, [N, N, -1])
		relative_position_bias = tf.transpose(relative_position_bias, perm=[2, 0, 1])
		relative_position_bias = tf.cast(relative_position_bias, attn.dtype)
		attn = attn + relative_position_bias[None, ...]
		
		if mask is not None:
			nW = tf.shape(mask)[0]
			mask = tf.cast(mask, dtype=attn.dtype)
			attn = tf.reshape(attn, [B_ // nW, nW, self.num_heads, N, N]) + mask[:, None, :, :]
			attn = tf.reshape(attn, [-1, self.num_heads, N, N])
		
		attn = tf.nn.softmax(attn, axis=-1)
		attn = self.attn_drop(attn)
		
		x = tf.linalg.matmul(attn, v)
		x = tf.transpose(x, perm=[0, 2, 1, 3])
		x = tf.reshape(x, [B_, N, C])
		x = self.proj(x)
		x = self.proj_drop(x)
		return x
	
	def get_config(self):
		config = super().get_config()
		config.update(
			{
				"dim": self.dim,
				"window_size": self.window_size,
				"num_heads": self.num_heads,
			}
		)
		return config


class PatchEmbed(layers.Layer):
	""" Video to Patch Embedding.

	Args:
		patch_size (int): Patch token size. Default: (2,4,4).
		embed_dim (int): Number of linear projection output channels. Default: 96.
		norm_layer (keras.layers, optional): Normalization layer. Default: None
	"""
	
	def __init__(
		self,
		patch_size=(1, 4, 4),
		embed_dim=96,
		**kwargs
	):
		super().__init__(**kwargs)
		self.patch_size = patch_size
		self.embed_dim = embed_dim
		
		# layers
		self.proj = layers.Conv3D(
			embed_dim,
			kernel_size=patch_size,
			strides=patch_size,
			name='embed_proj'
		)
		self.norm = layers.LayerNormalization(epsilon=1e-6)
	
	def build(self, input_shape):
		self.pads = [
			[0, 0],
			self._compute_padding(input_shape[1], self.patch_size[0]),
			self._compute_padding(input_shape[2], self.patch_size[1]),
			self._compute_padding(input_shape[3], self.patch_size[2]),
			[0, 0]
		]
		super().build(input_shape)
	
	def get_config(self):
		config = super(PatchEmbed, self).get_config()
		config.update({
			"patch_size": self.patch_size,
			"embed_dim": self.embed_dim,
		})
		return config
	
	def _compute_padding(self, dim, patch_size):
		pad_amount = patch_size - (dim % patch_size)
		return [
			0, pad_amount if pad_amount != patch_size else 0
		]
	
	def call(self, x):
		x = tf.pad(x, self.pads)
		x = self.proj(x)
		x = self.norm(x)
		return x


class PatchMerging(layers.Layer):
	""" Patch Merging Layer

	Args:
		dim (int): Number of input channels.
	"""
	
	def __init__(
		self,
		dim,
		**kwargs
	):
		super().__init__(**kwargs)
		self.dim = dim
		self.reduction = layers.Dense(2 * dim, use_bias=False)
		self.norm = layers.LayerNormalization(epsilon=1e-6)
	
	def call(self, x):
		""" call function.

		Args:
			x: Input feature, tensor size (B, D, H, W, C).
		"""
		input_shape = tf.shape(x)
		H, W = (
			input_shape[2],
			input_shape[3],
		)
		
		# padding if needed
		paddings = [
			[0, 0],
			[0, 0],
			[0, tf.math.floormod(H, 2)],
			[0, tf.math.floormod(W, 2)],
			[0, 0]
		]
		x = tf.pad(x, paddings)
		
		x0 = x[:, :, 0::2, 0::2, :]  # B D H/2 W/2 C
		x1 = x[:, :, 1::2, 0::2, :]  # B D H/2 W/2 C
		x2 = x[:, :, 0::2, 1::2, :]  # B D H/2 W/2 C
		x3 = x[:, :, 1::2, 1::2, :]  # B D H/2 W/2 C
		x = tf.concat([x0, x1, x2, x3], axis=-1)  # B D H/2 W/2 4*C
		x = self.norm(x)
		x = self.reduction(x)
		return x


class SwinTransformerBlock(layers.Layer):
	""" Swin Transformer Block.

	Args:
		dim (int): Number of input channels.
		num_heads (int): Number of attention heads.
		window_size (tuple[int]): Window size.
		shift_size (tuple[int]): Shift size for SW-MSA.
		mlp_ratio (float): Ratio of mlp hidden dim to embedding dim.
		qkv_bias (bool, optional): If True, add a learnable bias to query, key, value. Default: True
		qk_scale (float | None, optional): Override default qk scale of head_dim ** -0.5 if set.
		drop (float, optional): Dropout rate. Default: 0.0
		attn_drop (float, optional): Attention dropout rate. Default: 0.0
		drop_path (float, optional): Stochastic depth rate. Default: 0.0
		act_layer (keras.layers.Activation, optional): Activation layer. Default: gelu
		norm_layer (keras.layers, optional): Normalization layer.  Default: LayerNormalization
	"""
	
	def __init__(
		self,
		dim,
		num_heads,
		window_size=(2, 7, 7),
		shift_size=(0, 0, 0),
		mlp_ratio=4.,
		qkv_bias=True,
		qk_scale=None,
		drop=0.,
		attn_drop=0.,
		drop_path=0.,
		**kwargs
	):
		super().__init__(**kwargs)
		self.dim = dim
		self.num_heads = num_heads
		self.window_size = window_size
		self.shift_size = shift_size
		self.mlp_ratio = mlp_ratio
		mlp_hidden_dim = int(dim * mlp_ratio)
		
		assert 0 <= self.shift_size[0] < self.window_size[0], "shift_size must in 0-window_size"
		assert 0 <= self.shift_size[1] < self.window_size[1], "shift_size must in 0-window_size"
		assert 0 <= self.shift_size[2] < self.window_size[2], "shift_size must in 0-window_size"
		
		# layers
		self.norm1 = layers.LayerNormalization(epsilon=1e-6)
		self.attn = WindowAttention3D(
			dim,
			window_size=window_size,
			num_heads=num_heads,
			qkv_bias=qkv_bias,
			qk_scale=qk_scale,
			attn_drop=attn_drop,
			proj_drop=drop
		)
		self.drop_path1 = (
			StochasticDepth(drop_path)
			if drop_path > 0.0
			else layers.Activation("linear")
		)
		self.drop_path2 = (
			StochasticDepth(drop_path)
			if drop_path > 0.0
			else layers.Activation("linear")
		)
		self.norm2 = layers.LayerNormalization(epsilon=1e-6)
		self.mlp = Mlp(
			out_features=dim,
			hidden_features=mlp_hidden_dim,
			drop=drop
		)
	
	def build(self, input_shape):
		self.window_size, self.shift_size = get_window_size(
			input_shape[1:-1], self.window_size, self.shift_size
		)
		if any(i > 0 for i in self.shift_size):
			self.roll = True
		else:
			self.roll = False
		
		super().build(input_shape)
	
	def first_forward(self, x, mask_matrix):
		input_shape = tf.shape(x)
		B, D, H, W, C = (
			input_shape[0],
			input_shape[1],
			input_shape[2],
			input_shape[3],
			input_shape[4],
		)
		window_size, shift_size = self.window_size, self.shift_size
		x = self.norm1(x)
		
		# pad feature maps to multiples of window size
		pad_l = pad_t = pad_d0 = 0
		pad_d1 = tf.math.floormod(-D + window_size[0], window_size[0])
		pad_b = tf.math.floormod(-H + window_size[1], window_size[1])
		pad_r = tf.math.floormod(-W + window_size[2], window_size[2])
		paddings = [[0, 0], [pad_d0, pad_d1], [pad_t, pad_b], [pad_l, pad_r], [0, 0]]
		x = tf.pad(x, paddings)
		
		input_shape = tf.shape(x)
		Dp, Hp, Wp = (
			input_shape[1],
			input_shape[2],
			input_shape[3],
		)
		
		# Cyclic Shift
		if self.roll:
			shifted_x = tf.roll(
				x,
				shift=(-shift_size[0], -shift_size[1], -shift_size[2]),
				axis=(1, 2, 3)
			)
			attn_mask = mask_matrix
		else:
			shifted_x = x
			attn_mask = None
		
		# partition windows
		x_windows = window_partition(shifted_x, window_size)
		attn_windows = self.attn(x_windows, mask=attn_mask)
		
		# reverse the swin windows
		shifted_x = window_reverse(
			attn_windows, window_size, B, Dp, Hp, Wp
		)
		
		# Reverse Cyclic Shift
		if self.roll:
			x = tf.roll(
				shifted_x,
				shift=(shift_size[0], shift_size[1], shift_size[2]),
				axis=(1, 2, 3)
			)
		else:
			x = shifted_x
		
		# pad if required
		do_pad = tf.logical_or(
			tf.greater(pad_d1, 0),
			tf.logical_or(tf.greater(pad_r, 0), tf.greater(pad_b, 0))
		)
		x = tf.cond(
			do_pad,
			lambda: x[:, :D, :H, :W, :],
			lambda: x
		)
		return self.drop_path1(x)
	
	def get_config(self):
		config = super(SwinTransformerBlock, self).get_config()
		config.update({
			"dim": self.dim,
			"num_heads": self.num_heads,
			"window_size": self.window_size,
			"shift_size": self.shift_size,
			"mlp_ratio": self.mlp_ratio,
		})
		return config
	
	def call(self, inputs, mask_matrix=None):
		
		shortcut = inputs
		# x = self.norm2(inputs)
		x = self.mlp(inputs)
		x = shortcut + x #tf.cast(self.drop_path2(x), shortcut.dtype)
		return x


class Swin(Base, ABC):
	def __init__(
		self,
		hidden_size=128,
		patches=(1, 4, 4),
		window_size=(1, 7, 7),
		heads=(2, 2, 18, 2),
		repeats=(4, 8, 16, 32),
		expand_rate=4,
		qkv_bias=True,
		qk_scale=None,
		dropout_rate=0,
		attn_drop_rate=0.,
		drop_path_rate=0.1,
		**kwargs
	):
		super().__init__(**kwargs)
		self.hidden_size = hidden_size
		self.patches = patches
		self.window_size = window_size
		self.shift_size = tuple([i // 2 for i in window_size])
		self.heads = heads
		self.repeats = repeats
		self.mlp_ratio = expand_rate
		self.num_layers = len(repeats)
		self.qkv_bias = qkv_bias
		self.qk_scale = qk_scale
		self.dropout_rate = dropout_rate
		self.attn_drop_rate = attn_drop_rate
		self.drop_path_rate = drop_path_rate
		self.dpr = np.linspace(0., self.drop_path_rate, sum(self.repeats)).tolist()
		self.avg_pool = layers.GlobalAveragePooling3D()

	def compute_dim_padded(self, input_dim, window_dim_size):
		input_dim = tf.cast(input_dim, dtype=tf.float32)
		window_dim_size = tf.cast(window_dim_size, dtype=tf.float32)
		return tf.cast(
			tf.math.ceil(input_dim / window_dim_size) * window_dim_size,
			tf.int32
		)
	
	def call(self, inputs, **kwargs):
		
		m = PatchEmbed(
			patch_size=self.patches,
			embed_dim=self.hidden_size,
		)(inputs)
		
		for i in range(self.num_layers):
			drop_rates = self.dpr[sum(self.repeats[:i]): sum(self.repeats[:i + 1])]
			
			window_size, shift_size = get_window_size(
				m.shape[1:-1], self.window_size, self.shift_size
			)
			Dp = self.compute_dim_padded(m.shape[1], window_size[0])
			Hp = self.compute_dim_padded(m.shape[2], window_size[1])
			Wp = self.compute_dim_padded(m.shape[3], window_size[2])
			attn_mask = compute_mask(Dp, Hp, Wp, window_size, shift_size)
			
			for blk in range(self.repeats[i]):
				m = SwinTransformerBlock(
					dim=int(self.hidden_size * 2 ** i),
					num_heads=self.heads[i],
					window_size=self.window_size,
					mlp_ratio=self.mlp_ratio,
					qkv_bias=self.qkv_bias,
					qk_scale=self.qk_scale,
					drop=self.dropout_rate,
					attn_drop=self.attn_drop_rate,
					drop_path=drop_rates[blk],
					shift_size=(0, 0, 0) if (blk % 2 == 0) else self.shift_size,
				)(m, attn_mask)
			
			if i < self.num_layers - 1:
				m = PatchMerging(dim=int(self.hidden_size * 2 ** i))(m)
		
		m = self.avg_pool(m)
		return self.regressor(m)
